import turtle

turtle.shape("turtle")

turtle.fillcolor('green')

turtle.begin_fill() # I'm about to start a new shape
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.end_fill() # I'm done with the shape--fill it in now!
